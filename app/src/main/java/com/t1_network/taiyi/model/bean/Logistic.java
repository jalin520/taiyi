public class Logistic {
    private String logisticId;
    private String logisticState;
    private String logisticDetails;
    private String logisticTime;

    public String getLogisticId() {
        return logisticId;
    }

    public void setLogisticId(String logisticId) {
        this.logisticId = logisticId;
    }

    public String getLogisticState() {
        return logisticState;
    }

    public void setLogisticState(String logisticState) {
        this.logisticState = logisticState;
    }

    public String getLogisticDetails() {
        return logisticDetails;
    }

    public void setLogisticDetails(String logisticDetails) {
        this.logisticDetails = logisticDetails;
    }

    public String getLogisticTime() {
        return logisticTime;
    }

    public void setLogisticTime(String logisticTime) {
        this.logisticTime = logisticTime;
    }
}